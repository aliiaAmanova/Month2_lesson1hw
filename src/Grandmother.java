public class Grandmother {
    private int age;
    private String name;
    private String character;

    public Grandmother(int age, String name, String character) {
        this.age = age;
        this.name = name;
        this.character = character;
    }

    public int getAge() {
        return age;
    }

    public String getName() {
        return name;
    }



    public String getCharacter() {
        return character;
    }
}
